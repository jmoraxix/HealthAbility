package healthAbility.modelo;

/**
 * @author Diego Mar 17, 2014
 *
 */
public class Educacion {
	/****VARIABLES****/
	private String titulo;
	private String universidad;
	private String pais_universidad;
	private String fecha_conclusion;
	
	
	
	/*****GETTERS AND SETTERS*****/
	
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getUniversidad() {
		return universidad;
	}
	public void setUniversidad(String universidad) {
		this.universidad = universidad;
	}
	public String getPais_universidad() {
		return pais_universidad;
	}
	public void setPais_universidad(String pais_universidad) {
		this.pais_universidad = pais_universidad;
	}
	public String getFecha_conclusion() {
		return fecha_conclusion;
	}
	public void setFecha_conclusion(String fecha_conclusion) {
		this.fecha_conclusion = fecha_conclusion;
	}
	
	

}
